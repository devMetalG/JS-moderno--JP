const n1 = 20;
const n2 = "20";
const n3 = 30;

// Revisar si 2 numeros son iguales

console.log(n1 == n3);
console.log(n1 == n2);

// Comparador estricto 

console.log(n1 === n2);
console.log(n1 === parseInt(n2));

// Diferentes

const pas1 = "admin";
const pas2 = "Admin";

console.log(pas1 != pas2);
console.log(n1 != n2);
console.log(n1 !== n2);
console.log(n1 !== parseInt(n2));